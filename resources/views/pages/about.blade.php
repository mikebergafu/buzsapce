    <h1>About BuzSpace</h1>
    <p>BuzSpace is a platform that connects entrepreneurs who need retail space with shop owners who have space to share.</p>
    <h2>Our Mission</h2>
    <p>To make retail space accessible to every entrepreneur in Ghana. No long-term leases, no huge upfront costs — just flexible spaces where businesses can thrive.</p>
    <h2>The Problem We Solve</h2>
    <p>Many shop owners have unused counters, shelves, or sections in their stores. At the same time, thousands of entrepreneurs need affordable retail space to sell their products. BuzSpace bridges this gap.</p>

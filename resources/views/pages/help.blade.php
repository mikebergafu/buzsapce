    <h1>Help Center</h1>
    <p>Find answers to common questions below. Can't find what you're looking for? <a href="/contact">Contact us</a>.</p>

    <h2>Getting Started</h2>
    <details><summary>How do I create an account?</summary><p>Download the BuzSpace app, enter your phone number or email, and verify with the code sent to you. That's it — no passwords needed.</p></details>
    <details><summary>Is BuzSpace free to use?</summary><p>Yes! BuzSpace is free to download and use. We only charge a small service fee when a booking is confirmed.</p></details>

    <h2>Listing a Space</h2>
    <details><summary>How do I list my shop space?</summary><p>Tap "List a Space" in the app, upload photos, set your dimensions and pricing, and publish. Your listing goes live immediately.</p></details>
    <details><summary>Can I set different pricing options?</summary><p>Yes. You can offer daily, monthly, or commission-based pricing — or all three.</p></details>

    <h2>Finding a Space</h2>
    <details><summary>How do I find spaces near me?</summary><p>The app uses your location to show nearby available spaces. You can also search by area name or filter by price and size.</p></details>
    <details><summary>Can I contact the space owner before booking?</summary><p>Yes. Use the in-app messaging feature to chat directly with the space owner.</p></details>

    <h2>Account & Security</h2>
    <details><summary>How do I delete my account?</summary><p>Go to Profile → Settings → Delete Account. This permanently removes all your data.</p></details>
    <details><summary>Is my data safe?</summary><p>Yes. We use industry-standard encryption and never sell your personal data. See our <a href="/privacy">Privacy Policy</a> for details.</p></details>
